#include "math_functions.h"

int MathFunctions::isEqual(int a, int b) {
  return a == b;
}
int MathFunctions::isNotEqual(int a, int b) {
  return a != b;
}
int MathFunctions::add(int a, int b) {
  return a + b;
}
int MathFunctions::subtract(int a, int b) {
  return a - b;
}
int MathFunctions::multiply(int a, int b) {
  return a * b;
}
int MathFunctions::divide(int a, int b) {
  return a / b;
}